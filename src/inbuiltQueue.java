
import java.util.*;

public class inbuiltQueue {
	
	public static void main(String[] args) {
		Queue<Integer> queue = new LinkedList<>();
		queue.add(5);
		queue.add(15);
		queue.add(6545);
		queue.add(57);
		queue.add(125);
		
		queue.peek();
		
		queue.remove();
		System.out.println(queue.remove());
		
		
	}

	
}
